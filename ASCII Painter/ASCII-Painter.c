void importProject(void){
    char projectName[100],extension[5] = ".tbp";
    gotoxy(50,23);
    printf("\t[IMPORT] Project Name: ");
    scanf("%s",projectName);
    strcat(projectName,extension);
    char importBuffer[5000];
    int importBufferNum[1152];
    char *endPtr = importBuffer;//reminder: always initialize pointers to the target address if it's used to traverse an array
    int index = 0;
    FILE *fIN;
    if(fIN = fopen(projectName,"r")){
        fgets(importBuffer,5000,fIN);
        while(*endPtr != '\0'){
            long int num = strtol(endPtr,&endPtr,10);
            while (*endPtr == ' '){//skip any whitespace
                endPtr++;
            }
            importBufferNum[index] = num;
            index++;
        }
        index = 0;
        for(a = 0; a < row; a++){
            for(b = 0; b < column; b++){//format is <char> <color>
                charData[a][b] = importBufferNum[index];
                colorData[a][b] = importBufferNum[index+1];
                index+=2;
            }
        }
        fclose(fIN);
    }
    else{
        readError = 1;
        return;
    }
}

