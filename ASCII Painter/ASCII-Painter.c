void painter(void){
    system("cls");
    system("COLOR F0");
    hideCursor();
    if (isLoaded == 0){//executes only once
        system("COLOR F0");
        loading();
        system("cls");
        initializeColorPalette();
        for (a=0; a<row; a++){
            for (b=0; b<column; b++){
                charData[a][b] = 176;
                colorData[a][b] = ((a % 2 == 0 && b % 2 == 0) || (a % 2 == 1 && b % 2 == 1) ? 79 : 81);
            }
        }
    }
    displayCanvas();
    readError = 0;
    gotoxy(x,y);//x,y = locations on the console (not array indices)
    printf("%c",16);
    userInput = toupper(getch());
    last_input = userInput;
    switch(userInput){ // 1 step in x, = 2 steps, since character 219 is 0.5 pixel wide
        case 'W': y--; if (y < 4 ){y = 3;} break;
        case 'A': x-=2; if (x < 7){x = 6;} break;
        case 'S': y++; if (y > 26){y = 26;} break;
        case 'D': x+=2; if (x > 52){x = 52;} break;
        case 'C': isLoaded = 1; isInColorSelector = 1; gotoxy(cx,cy); colorSelector(); break;
        case 'X': colorNum = colorData[y-3][(x/2)-3]; break;
        case 'P': isSaved = 0; charData[y-3][(x/2)-3] = 219; colorData[y-3][(x/2)-3] = colorNum; break;
        case 'R': isSaved = 0; charData[y-3][(x/2)-3] = 176; colorData[y-3][(x/2)-3] = ((y-3) % 2 == 0 && ((x/2)-3) % 2 == 0) || ((y-3) % 2 == 1 && ((x/2)-3) % 2 == 1) ? 79 : 81; break; //remove the pixel pointed by cursor, and  reset the color associated with that position
        case 'Q': menu(); break;//quit
        case 'E': exportProject(); break;
        case 'I': importProject(); break;
        default: painter();
    }
    isLoaded = 1;
    painter();
}

void initializeColorPalette(void){
    for (a = 0; a < 7; a++){
        for (b = 0; b < 12; b++){
            colorPalette[a][b] = (a*12) + b;
        }
    }
}

void colorSelector(void){
    system("cls");
    displayCanvas();
    gotoxy(cx,cy);
    printf("%c",16);
    userInput = toupper(getch());
    last_input = userInput;
    switch(userInput){
        case 'W': cy--; if (cy < 3 ){cy = 3;} break;
        case 'A': cx-=2; if (cx < 80){cx = 80;} break;
        case 'S': cy++; if (cy > 9){cy = 9;} break;
        case 'D': cx+=2; if (cx > 102){cx = 102;} break;
        case 'C': isInColorSelector = 0; painter(); break;
        default: colorSelector();
    }
    colorNum = colorPalette[cy-3][(cx/2)-40];
    colorSelector();
}

